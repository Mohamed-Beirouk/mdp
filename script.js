// Function to change language
function changeLanguage(lang) {
    const html = document.documentElement;
    html.lang = lang;
    html.dir = lang === 'ar' ? 'rtl' : 'ltr';
    
    // Update all text elements
    document.getElementById('site-title').textContent = translations[lang].header.title;
    document.getElementById('hero-title').textContent = translations[lang].hero.title;
    document.getElementById('hero-subtitle').textContent = translations[lang].hero.subtitle;
    document.getElementById('about-title').textContent = translations[lang].about.title;
    document.getElementById('about-content').textContent = translations[lang].about.content;
    document.getElementById('activities-title').textContent = translations[lang].activities.title;
    document.getElementById('awareness-title').textContent = translations[lang].activities.awareness.title;
    document.getElementById('awareness-content').textContent = translations[lang].activities.awareness.content;
    document.getElementById('early-detection-title').textContent = translations[lang].activities.earlyDetection.title;
    document.getElementById('early-detection-content').textContent = translations[lang].activities.earlyDetection.content;
    document.getElementById('school-visits-title').textContent = translations[lang].activities.schoolVisits.title;
    document.getElementById('school-visits-content').textContent = translations[lang].activities.schoolVisits.content;
    document.getElementById('team-title').textContent = translations[lang].team.title;
    
    const teamMembers = document.getElementById('team-members');
    teamMembers.innerHTML = '';
    translations[lang].team.members.forEach(member => {
        const li = document.createElement('li');
        li.textContent = member;
        teamMembers.appendChild(li);
    });
    
    document.getElementById('contact-title').textContent = translations[lang].contact.title;
    document.getElementById('contact-address').textContent = translations[lang].contact.address;
    document.getElementById('contact-email').textContent = translations[lang].contact.email;
    document.getElementById('contact-phone').textContent = translations[lang].contact.phone;
    document.getElementById('whatsapp-number').textContent = translations[lang].contact.whatsapp;
    document.getElementById('footer-text').textContent = translations[lang].footer.text;

    document.getElementById('official-title').textContent = translations[lang].official.title;
    
    const boardList = document.querySelector('.info-item:nth-child(6) ul');
    boardList.innerHTML = '';
    translations[lang].official.board.forEach(member => {
        const li = document.createElement('li');
        li.textContent = member;
        boardList.appendChild(li);
    });
    
    // Update other official info items
    const officialItems = document.querySelectorAll('.info-item');
    officialItems[0].querySelector('p').textContent = translations[lang].official.legal;
    officialItems[1].querySelector('p').textContent = translations[lang].official.creation;
    officialItems[2].querySelector('p').textContent = translations[lang].official.license;
    officialItems[3].querySelector('p').textContent = translations[lang].official.type;
    officialItems[4].querySelector('p').textContent = translations[lang].official.domains;
}

